
#集合set

mylist=[1,1,1,2,"zzz"]      #列表可以重复,可以包含不同类型元素
print(mylist)

朱建涛的粉丝=set(["大傻子","二傻子","熊大","光头强","熊二"])
print(朱建涛的粉丝)       #列表转集合并打印

朱建涛的面条=set(("牛皮","杏仁儿","高圆圆","大傻子","牛皮"))
print(朱建涛的面条)       #元组转集合并打印,集合可以去重

print("交集",朱建涛的面条 & 朱建涛的粉丝)  #交集
print("并集",朱建涛的面条 | 朱建涛的粉丝)  #并集
print("差集",朱建涛的面条 - 朱建涛的粉丝)  #差集 我有你没有
print("差集",朱建涛的粉丝 - 朱建涛的面条)  #差集
