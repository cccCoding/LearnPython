
#字典dict 键值对
#字典有一个映射关系
#字典不能重复

朱建涛的面条={"牛皮":12,"杏仁儿":1,"高圆圆":2,"大傻子":1}
print(type(朱建涛的面条),朱建涛的面条)

朱建涛的面条["熊大"]=100    #没有就创建
print(朱建涛的面条)
print(朱建涛的面条.keys())    #.keys()返回的不是列表,需要转换
print(朱建涛的面条.values())
print(list(朱建涛的面条.keys()))      #数据类型转换
print(tuple(朱建涛的面条.keys()))

朱建涛的面条["熊大"]=10    #有就刷新
print(朱建涛的面条)